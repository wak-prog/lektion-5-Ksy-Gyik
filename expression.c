#include <stdio.h>
#include <stdlib.h>


int main() {
	
	int a=-1;
	int b=-1;
	int c=-1;
	int d=-1;
	int erg=1;
	int ab;
	int cd;
	
	printf("Bitte 4 Zahlen eingeben:\n");
	scanf("%d %d %d %d",&a,&b,&c,&d);
	
	ab=a*b;
	cd=c*d;
	erg=(a*b)+(c*d);
	
	printf("Das Ergebnis von %d+%d=%d",ab,cd,erg);
	
	return 0;
}

